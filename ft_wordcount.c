int	ft_wordcount(char const *s, char c)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		while (s[i] != c && s[i] != '\0')
			i++;
		while (s[i] == c)
			i++;
		n++;
	}
	return (n);
}
