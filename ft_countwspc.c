int	ft_countwspc(char const *s)
{
	int		i;
	int		n;

	i = 0;
	n = 0;
	if (s[i] == '\0')
		return (n);
	while ((s[i] > 8 && s[i] < 14 && s[i] != '\0') || s[i] == 32)
	{
		i++;
		n++;
	}
	if (s[i] == '\0')
		return (n);
	while (s[i] != '\0')
		i++;
	while ((s[i] > 8 && s[i] < 14) || s[i] == 32 || s[i] == '\0')
	{
		i--;
		n++;
	}
	return (n - 1);
}
