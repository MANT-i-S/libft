char	*ft_itoarev(int n, char *dest)
{
	int		i;
	int		mod;
	int		sign;

	i = 0;
	if (n < 0)
	{
		n = -n;
		sign = 1;
	}
	while (n >= 10)
	{
		mod = n % 10;
		n = n / 10;
		dest[i++] = mod + 48;
	}
	dest[i++] = n + 48;
	if (sign == 1)
		dest[i++] = '-';
	dest[i] = '\0';
	return (dest);
}
