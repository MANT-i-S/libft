int	ft_findstart(const char *s, char c, int i)
{
	int start;

	start = i;
	if (i == 0 && s[i] != c)
		return (start);
	while ((s[i] != '\0' && s[i - 1] != c) || s[i] == c)
	{
		i++;
		start++;
	}
	return (start);
}
